          <h1 id="main">What is MVS?</h1>

          <p class="lead"><a href="https://en.wikipedia.org/wiki/MVS"
          target="_blank" rel="nofollow" title="Multiple Virtual
          Storage">MVS</a>, which stands for <a
          href="https://en.wikipedia.org/wiki/MVS" target="_blank"
          rel="nofollow" title="Multiple Virtual Storage">Multiple
          Virtual Storage</a>, is one of the oldest <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">operating systems</a> <a
          href="https://en.wikipedia.org/wiki/Software_development_process"
          target="_blank" rel="nofollow" title="Software development
          process">developed</a> by <a
          href="https://en.wikipedia.org/wiki/IBM" target="_blank"
          rel="nofollow" title="IBM">IBM</a> and distant relative of
          what we nowadays call <a href="/dos.php" target="_self"
          rel="dofollow" title="Disk Operating System">DOS</a>.  <a
          href="https://en.wikipedia.org/wiki/MVS" target="_blank"
          rel="nofollow" title="Multiple Virtual Storage">MVS</a> has
          its own <a
          href="https://en.wikipedia.org/wiki/Programming_language"
          target="_blank" rel="nofollow" title="Programming
          language">programming language</a>, <a
          href="https://en.wikipedia.org/wiki/Job_Control_Language"
          target="_blank" rel="nofollow" title="Job Control
          Language">JCL</a> (<a
          href="https://en.wikipedia.org/wiki/Job_Control_Language"
          target="_blank" rel="nofollow" title="Job Control
          Language">Job Control Language</a>).  Three decades after <a
          href="https://en.wikipedia.org/wiki/MVS" target="_blank"
          rel="nofollow" title="Multiple Virtual Storage">MVS</a> was
          created (1974), <a
          href="https://en.wikipedia.org/wiki/Job_Control_Language"
          target="_blank" rel="nofollow" title="Job Control
          Language">JCL</a> is still heavily used.  As a matter of fact,
          where <a href="/frn2000.php" target="_self" rel="author"
          title="FRN2000 for VintageOS v133C971">I</a> work, some <a
          href="https://en.wikipedia.org/wiki/Programmer"target="_blank"
          rel="nofollow" title="Programmer">programmers</a> still use <a
          href="https://en.wikipedia.org/wiki/Job_Control_Language"
          target="_blank" rel="nofollow" title="Job Control
          Language">JCL</a> on a daily basis in the <a
          href="https://en.wikipedia.org/wiki/Z%2FVM" target="_blank"
          rel="nofollow" title="Z/VM">IBM z/VM</a> system.  Some of the
          <a href="https://en.wikipedia.org/wiki/Application_software"
          target="_blank" rel="nofollow" title="Application
          software">applications</a> where <a href="/frn2000.php"
          target="_self" rel="author" title="FRN2000 for VintageOS
          v133C971">I</a> work query <a
          href="https://en.wikipedia.org/wiki/Virtual_Storage_Access_Method"
          target="_blank" rel="nofollow" title="Virtual Storage Access
          Method">VSAM</a> <a
          href="https://en.wikipedia.org/wiki/Computer_file"
          target="_blank" rel="nofollow" title="Computer file">files</a>
          and third-party <a
          href="https://en.wikipedia.org/wiki/Application_software"
          target="_blank" rel="nofollow" title="Application
          software">application</a> handle <a
          href="https://en.wikipedia.org/wiki/Data_scraping"
          target="_blank" rel="nofollow" title="Data scraping">screen
          scraping</a> to replicate the screens and output to <a
          href="https://en.wikipedia.org/wiki/Active_server_pages"
          target="_blank" rel="nofollow" title="Active Server
          Pages">ASP</a> or <a
          href="https://en.wikipedia.org/wiki/JavaServer_Pages"
          target="_blank" rel="nofollow" title="JavaServer
          Pages">JSP</a>.  After over forty years, the <a
          href="https://en.wikipedia.org/wiki/Mainframe_computer"
          target="_blank" rel="nofollow" title="Mainframe
          computer">mainframe computers</a> are still king.</p>

