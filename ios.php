          <h2 id="Installation">Installing iOS:</h2>

          <p class="lead">There is no <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating system">OS</a>
          <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">installation</a> that a <a
          href="https://en.wikipedia.org/wiki/User_%28computing%29"
          target="_blank" rel="nofollow" title="User
          (computing)">user</a> can perform.  At the same time, all <a
          href="https://en.wikipedia.org/wiki/Patch_%28computing%29"
          target="_blank" rel="nofollow" title="Patch
          (computing)">updates</a> for <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating system">OS</a>
          and <a href="https://en.wikipedia.org/wiki/Mobile_app"
          target="_blank" rel="nofollow" title="Mobile app">apps</a> (<a
          href="https://en.wikipedia.org/wiki/Mobile_app"
          target="_blank" rel="nofollow" title="Mobile app">mobile
          applications</a>) are pushed by <a
          href="https://en.wikipedia.org/wiki/Apple_Inc."
          target="_blank" rel="nofollow" title="Apple Inc.">Apple</a>,
          but authorized by the <a
          href="https://en.wikipedia.org/wiki/User_%28computing%29"
          target="_blank" rel="nofollow" title="User
          (computing)">user</a>.  Note that <a
          href="https://en.wikipedia.org/wiki/IOS_6#Problems"
          target="_blank" rel="nofollow" title="iOS 6, Problems">iOS
          6</a> on <a href="https://en.wikipedia.org/wiki/IPhone"
          target="_blank" rel="nofollow" title="iPhone">iPhone</a> and
          <a href="https://en.wikipedia.org/wiki/IPod" target="_blank"
          rel="nofollow" title="iPod">iPod</a> is fairly <a
          href="https://en.wikipedia.org/wiki/Software_bug"
          target="_blank" rel="nofollow" title="Software bug">buggy</a>.
          The <a
          href="https://en.wikipedia.org/wiki/Patch_%28computing%29"
          target="_blank" rel="nofollow" title="Patch
          (computing)">update</a> drains the unit's battery in a matter
          of an hour or so.  If you are running <a
          href="https://en.wikipedia.org/wiki/IOS" target="_blank"
          rel="nofollow" title="iOS">iOS 5</a>, there is nothing more
          you can do as there is no <a
          href="https://en.wikipedia.org/wiki/IOS" target="_blank"
          rel="nofollow" title="iOS">iOS 7</a> <a
          href="https://en.wikipedia.org/wiki/Patch_%28computing%29"
          target="_blank" rel="nofollow" title="Patch
          (computing)">update</a> for <a
          href="https://en.wikipedia.org/wiki/IPhone" target="_blank"
          rel="nofollow" title="iPhone">iPhone</a> and <a
          href="https://en.wikipedia.org/wiki/IPod" target="_blank"
          rel="nofollow" title="iPod">iPod</a>.  This does not seem to
          be a problem in <a href="https://en.wikipedia.org/wiki/IPad"
          target="_blank" rel="nofollow" title="iPad">iPad</a>, which
          seems to run <a
          href="https://en.wikipedia.org/wiki/IOS" target="_blank"
          rel="nofollow" title="iOS">iOS 7</a> smoothly.</p>

          <p class="lead">At the same time, all <a
          href="https://en.wikipedia.org/wiki/Mobile_app"
          target="_blank" rel="nofollow" title="Mobile app">apps</a> are
          <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">installed</a> from <a
          href="https://en.wikipedia.org/wiki/Apple_Inc."
          target="_blank" rel="nofollow" title="Apple Inc.">Apple</a>'s
          <a href="https://en.wikipedia.org/wiki/App_Store_%28iOS%29"
          target="_blank" rel="nofollow" title="App Store (iOS)">App
          Store</a>.  This means that no vendor can <a
          href="https://en.wikipedia.org/wiki/Software_distribution"
          target="_self" rel="dofollow" title="Software
          distribution">distribute</a> any <a
          href="https://en.wikipedia.org/wiki/Software" target="_blank"
          rel="nofollow" title="Software">software</a> without <a
          href="https://en.wikipedia.org/wiki/Apple_Inc."
          target="_blank" rel="nofollow" title="Apple Inc.">Apple</a>
          checking it first for <a
          href="https://en.wikipedia.org/wiki/Malware"
          target="_blank" rel="nofollow" title="Malware">malware</a>
          and/or performance &#151; a fairly costly process, about $800
          for a <a
          href="https://en.wikipedia.org/wiki/Software_developer"
          target="_blank" rel="nofollow" title="Software
          developer">developer</a>'s <a
          href="https://en.wikipedia.org/wiki/License" target="_blank"
          rel="nofollow" title="License">license</a> (last time <a
          href="/frn2000.php" target="_self" rel="author" title="FRN2000
          for VintageOS v133C971">I</a> checked).</p>

          <p class="lead">Aside from downloading <a
          href="https://en.wikipedia.org/wiki/Computer_file"
          target="_blank" rel="nofollow" title="Computer file">files</a>
          from the <a href="https://en.wikipedia.org/wiki/Internet"
          target="_blank" rel="nofollow" title="Internet">internet</a>
          or from a service like <a
          href="https://en.wikipedia.org/wiki/Dropbox_%28service%29"
          target="_blank" rel="nofollow" title="Dropbox
          (service)">Dropbox</a> to an <a
          href="https://en.wikipedia.org/wiki/IPod" target="_blank"
          rel="nofollow" title="iPod">iPod</a>, <a
          href="https://en.wikipedia.org/wiki/IPhone" target="_blank"
          rel="nofollow" title="iPhone">iPhone</a> or <a
          href="https://en.wikipedia.org/wiki/IPad" target="_blank"
          rel="nofollow" title="iPad">iPad</a>, there is no access to
          the <a href="https://en.wikipedia.org/wiki/Computer_appliance"
          target="_blank" rel="nofollow" title="Computer
          appliance">device</a> <a
          href="https://en.wikipedia.org/wiki/Computer_data_storage"
          target="_blank" rel="nofollow" title="Computer data
          storage">storage</a> other than using the <a
          href="https://en.wikipedia.org/wiki/ITunes" target="_blank"
          rel="nofollow" title="iTunes">iTunes</a> <a
          href="https://en.wikipedia.org/wiki/Computer_program"
          target="_blank" rel="nofollow" title="Computer
          programming">program</a> for <a href="/macos.php"
          target="_self" rel="dofollow" title="macOS">Mac</a> and/or <a
          href="/windows.php" target="_self" rel="dofollow"
          title="Microsoft Windows">Windows</a>.  With <a
          href="https://en.wikipedia.org/wiki/ITunes" target="_blank"
          rel="nofollow" title="iTunes">iTunes</a>, you can merely
          transfer <a href="https://en.wikipedia.org/wiki/Multimedia"
          target="_blank" rel="nofollow"
          title="Multimedia">multimedia</a> to and from the <a
          href="https://en.wikipedia.org/wiki/Computer_appliance"
          target="_blank" rel="nofollow" title="Computer
          appliance">device</a>.</p>

          <p class="lead">As in the case of <a href="/android.php"
          target="_self" rel="dofollow" title="Android">Android</a> and
          probably any other <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">OS</a>, there are ways of <a
          href="https://en.wikipedia.org/wiki/Android_rooting"
          target="_blank" rel="nofollow" title="Android
          rooting">rooting</a> <a
          href="https://en.wikipedia.org/wiki/IPod" target="_blank"
          rel="nofollow" title="iPod">iPod</a>, <a
          href="https://en.wikipedia.org/wiki/IPhone" target="_blank"
          rel="nofollow" title="iPhone">iPhone</a> and <a
          href="https://en.wikipedia.org/wiki/IPad" target="_blank"
          rel="nofollow" title="iPad">iPad</a>.  Note that the latter
          might be considered illegal.</p>

