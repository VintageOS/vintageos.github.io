          <h1 id="main">What is BeOS?</h1>

          <p class="lead"><href="https://en.wikipedia.org/wiki/BeOS"
          target="_blank" rel="nofollow" title="BeOS">BeOS</a> was a <a
          href="https://en.wikipedia.org/wiki/Proprietary_software"
          target="_blank" rel="nofollow" title="Proprietary
          software">proprietary</a> (not <a href="/opensource.php"
          target="_self" rel="dofollow" title="Open Source">open
          source</a>) <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">OS</a>.  <href="https://en.wikipedia.org/wiki/BeOS"
          target="_blank" rel="nofollow" title="BeOS">BeOS</a> is very
          similar to <a href="/unix.php" target="_self" rel="dofollow"
          title="Unix">Unix</a> although it is not a <a
          href="https://en.wikipedia.org/wiki/Clone_%28computing%29"
          target="_blank" rel="nofollow" title="Clone
          (computing)">clone</a> or related <a
          href="https://en.wikipedia.org/wiki/Software_distribution"
          target="_self" rel="dofollow" title="Software
          distribution">distribution</a>.  Nowadays you can run <a
          href="https://en.wikipedia.org/wiki/Haiku_%28operating_system%29"
          target="_blank" rel="nofollow" title="Haiku (operating
          system)">Haiku</a>, which is covered on this page.</p>

