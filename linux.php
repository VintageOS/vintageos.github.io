          <h1 id="main">What is Linux?</h1>

          <p class="lead"><a href="https://en.wikipedia.org/wiki/Linux"
          target="_blank" rel="nofollow" title="Linux">Linux</a> is a <a
          href="/unix.php" title="Unix">UNIX&#169;</a> <a
          href="https://en.wikipedia.org/wiki/Clone_%28computing%29"
          target="_blank" rel="nofollow" title="Clone
          (computing)">clone</a> <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating system">OS</a>
          based on <a href="https://en.wikipedia.org/wiki/Minix"
          target="_blank" rel="nofollow" title="Minix">Minix</a>,
          created in 1991 by <a
          href="https://en.wikipedia.org/wiki/Linus_Torvalds"
          target="_blank" rel="nofollow" title="Linus Torvalds">Linus
          Torvalds</a> (<a
          href="https://en.wikipedia.org/wiki/University_of_Helsinki"
          target="_blank" rel="nofollow" title="University of
          Helsinki">University of Helsinki</a>).  <a
          href="https://en.wikipedia.org/wiki/Linus_Torvalds"
          target="_blank" rel="nofollow" title="Linus
          Torvalds">Torvalds</a> controls all changes and releases of
          the <a href="http://www.kernel.org/" target="_blank"
          rel="nofollow" title="The Linux Kernel Archives">Linux
          kernel</a>.</p>

          <p class="lead">For close to three decades, <a
          href="https://en.wikipedia.org/wiki/Linux" target="_blank"
          rel="nofollow" title="Linux">Linux</a> has been one of the
          most commonly used and most reliable <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">operating systems</a> for <a
          href="https://en.wikipedia.org/wiki/Server_%28computing%29"
          target="_blank" rel="nofollow" title="Server
          (computing)">servers</a> in the market, no matter what paid
          third-party reports that are plaguing the <a
          href="https://en.wikipedia.org/wiki/Internet"
          target="_blank" rel="nofollow" title="Internet">internet</a>
          indicate.</p>

          <p class="lead">Most <a
          href="https://en.wikipedia.org/wiki/Linux_distribution"
          target="_blank" rel="nofollow" title="Linux
          distribution">Linux distributions</a> include a collection of
          <a href="https://en.wikipedia.org/wiki/Computer_program"
