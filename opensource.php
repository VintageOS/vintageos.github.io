          <h1 id="main">What is Open Source?</h1>

          <p class="lead">The term <a
          href="https://en.wikipedia.org/wiki/Open-source_model"
          target="_blank" rel="nofollow" title="Open-source model">open
          source</a> (or simply referred to as <em>&#34;open&#34;</em>)
          indicates that the <a
          href="https://en.wikipedia.org/wiki/Source_code"
          target="_blank" rel="nofollow" title="Source code">source
          code</a> for specific <a
          href="https://en.wikipedia.org/wiki/Computer_program"
          target="_blank" rel="nofollow" title="Computer
          programming">programs</a> (<a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">OS</a>, <a
          href="https://en.wikipedia.org/wiki/Server_%28computing%29"
          target="_blank" rel="nofollow" title="Server
          (computing)">server</a>, <a
          href="https://en.wikipedia.org/wiki/Database" target="_blank"
          rel="nofollow" title="Database">database</a>, etc) is
          available to the public, that anyone may alter the <a
          href="https://en.wikipedia.org/wiki/Source_code"
          target="_blank" rel="nofollow" title="Source code">source
          code</a> but must submit the new <a
          href="https://en.wikipedia.org/wiki/Source_code"
          target="_blank" rel="nofollow" title="Source code">source
          code</a> back to the community and especially to the owner(s)
          of the project, yet regulated by any of various umbrella <a
          href="https://en.wikipedia.org/wiki/License" target="_blank"
          rel="nofollow" title="License">licenses</a> to protect the
          project and everyone involved in the <a
          href="https://en.wikipedia.org/wiki/Software_development_process"
          target="_blank" rel="nofollow" title="Software development
          process">development</a> or use of the <a
          href="https://en.wikipedia.org/wiki/Computer_program"
          target="_blank" rel="nofollow" title="Computer
          programming">program(s)</a>.</p>

          <p class="lead">The social benefit of any open project is that
          many <a
          href="https://en.wikipedia.org/wiki/Software_developer"
          target="_blank" rel="nofollow" title="Software
          developer">developers</a> may write (<a
          href="https://en.wikipedia.org/wiki/Source_code"
          target="_blank" rel="nofollow" title="Source code">code</a>)
          or fix specific sections of the <a
          href="https://en.wikipedia.org/wiki/Source_code"
          target="_blank" rel="nofollow" title="Source code">source
          code</a>.  Working as a community, no single <a
          href="https://en.wikipedia.org/wiki/Software_developer"
          target="_blank" rel="nofollow" title="Software
          developer">developer</a> owns it, but each receives credit for
          being part of such project.  At the same time, these projects
          are <a href="https://en.wikipedia.org/wiki/Gratis"
          target="_blank" rel="nofollow" title="Gratis">free of
          charge</a> to <a
          href="https://en.wikipedia.org/wiki/User_%28computing%29"
          target="_blank" rel="nofollow" title="User
          (computing)">users</a>, who often willingly supply feedback
          and submit <a href="https://en.wikipedia.org/wiki/Information"
          target="_blank" rel="nofollow"
          title="Information">information</a> on <a
          href="https://en.wikipedia.org/wiki/Software_bug"
          target="_blank" rel="nofollow" title="Software bug">bugs</a>
          on the functionality of the <a
          href="https://en.wikipedia.org/wiki/Computer_program"
          target="_blank" rel="nofollow" title="Computer
          programming">program(s)</a>.  In general, for almost every
          commercial <a
          href="https://en.wikipedia.org/wiki/Computer_program"
          target="_blank" rel="nofollow" title="Computer
          programming">program</a> there is an open and <a
          href="https://en.wikipedia.org/wiki/Royalty-free"
          target="_blank" rel="nofollow"
          title="Royalty-free">royalty-free</a> alternative.  The
          biggest examples nowadays are the various flavors of <a
          href="/linux.php" target="_self" rel="dofollow"
          title="Linux">Linux</a> and <a href="/bsd.php" target="_self"
          rel="dofollow" title="Berkeley Software Distribution">BSD</a>
          &#151; most likely in this order.</p>

          <p class="lead">The drawback of some <a
          href="https://en.wikipedia.org/wiki/Open-source_model"
          target="_blank" rel="nofollow" title="Open-source model">open
          source</a> projects is that there can be little support by the
          <a href="https://en.wikipedia.org/wiki/Software_developer"
          target="_blank" rel="nofollow" title="Software
          developer">developer(s)</a> for any number of reasons, which
          normally narrow down to lack resources &#151; much too often
          time and/or money.  Nonetheless other <a
          href="https://en.wikipedia.org/wiki/User_%28computing%29"
          target="_blank" rel="nofollow" title="User
          (computing)">users</a> are more than willing to help.</p>

          <p class="lead">At the same time, big projects like <a
          href="https://en.wikipedia.org/wiki/MySQL" target="_blank"
          rel="nofollow" title="MySQL">MySQL</a> make a revenue offering
          technical support although the product itself is <a
          href="https://en.wikipedia.org/wiki/Gratis" target="_blank"
          rel="nofollow" title="Gratis">free of charge</a>.  In other
          cases, third-parties offer commercial support for products
          like <a
          href="https://en.wikipedia.org/wiki/Apache_HTTP_Server"
          target="_blank" rel="nofollow" title="Apache HTTP
          Server">Apache</a>.  Of course, there is vast on-line and/or
          printed documentation and literature on these projects.</p>

          <p class="lead">In the other hand, we can use the term
          <em>&#34;<a
          href="https://en.wikipedia.org/wiki/Proprietary_software"
          target="_blank" rel="nofollow" title="Proprietary
          software">closed</a>&#34;</em> when referring to <a
          href="https://en.wikipedia.org/wiki/Proprietary_software"
          target="_blank" rel="nofollow" title="Proprietary
          software">proprietary code</a>, not available to the public,
          regulated and limited by legalities whether the <a
          href="https://en.wikipedia.org/wiki/Computer_program"
          target="_blank" rel="nofollow" title="Computer
          programming">program</a> is <a
          href="https://en.wikipedia.org/wiki/Gratis" target="_blank"
          rel="nofollow" title="Gratis">free of charge</a> or not.
          Understanding the latter, <a
          href="https://en.wikipedia.org/wiki/Proprietary_software"
          target="_blank" rel="nofollow" title="Proprietary
          software">closed</a> <a
          href="https://en.wikipedia.org/wiki/Computer_program"
          target="_blank" rel="nofollow" title="Computer
          programming">programs</a> can either be given to the public <a
          href="https://en.wikipedia.org/wiki/Gratis" target="_blank"
          rel="nofollow" title="Gratis">free of charge</a> (<a
          href="https://en.wikipedia.org/wiki/Freeware" target="_blank"
          rel="nofollow" title="Freeware">freeware</a>), included in <a
          href="https://en.wikipedia.org/wiki/Open-source_model"
          target="_blank" rel="nofollow" title="Open-source model">open
          source</a> projects, offered as a commercial product or
          marketed in any possible way or combination of ways including
          as <a href="https://en.wikipedia.org/wiki/Shareware"
          target="_blank" rel="nofollow" title="Shareware">shareware</a>
          at the sole discretion of the <a
          href="https://en.wikipedia.org/wiki/Software_developer"
          target="_blank" rel="nofollow" title="Software
          developer">developer(s)</a>.  In this case, there is more
          support alternatives from the vendor(s) and third-parties.</p>

          <p class="lead"><a href="/frn2000.php" target="_self"
          rel="author" title="FRN2000 for VintageOS v133C971">I</a>
          support <a
          href="https://en.wikipedia.org/wiki/Open-source_model"
          target="_blank" rel="nofollow" title="Open-source model">open
          source</a> as an extension of the <a
          href="https://en.wikipedia.org/wiki/Hacker_%28computer_security%29"
          target="_blank" rel="nofollow" title="Hacker (computer
          security)">hacker</a> mentality and its rebellious spirit,
          which offers <a href="https://en.wikipedia.org/wiki/Gratis"
          target="_blank" rel="nofollow" title="Gratis">free of
          charge</a> alternatives to commercialism, big corporations
          (somehow <a href="https://en.wikipedia.org/wiki/IBM"
          target="_blank" rel="nofollow" title="IBM">IBM</a> not often
          included), <a
          href="https://en.wikipedia.org/wiki/Proprietary_software"
          target="_blank" rel="nofollow" title="Proprietary
          software">proprietary code</a> <a
          href="https://en.wikipedia.org/wiki/Technology"
          target="_blank" rel="nofollow"
          title="Technology">technologies</a> and the <em>status
          quo</em> in general.  As <a href="/frn2000.php" target="_self"
          rel="dofollow" title="FRN2000 for VintageOS v133C971">I</a>
          have mentioned in other sections of <a href="/" target="_self"
          rel="author" title="VintageOS v133C971">VintageOS</a>, <a
          href="/frn2000.php" target="_self" rel="author" title="FRN2000
          for VintageOS v133C971">I</a> currently use <a
