          <h1 id="main">What is Palm OS?</h1>

          <p class="lead" id="2005">On late September 2005, <a
          href="https://en.wikipedia.org/wiki/Microsoft" target="_blank"
          rel="nofollow" title="Microsoft">Microsoft</a> and <a
          href="https://en.wikipedia.org/wiki/Palm,_Inc."
          target="_blank" rel="nofollow" title="Palm Inc.">Palm</a>
          agreed to sell new <a
          href="https://en.wikipedia.org/wiki/Smartphone"
          target="_blank" rel="nofollow" title="Smartphone">smart</a> <a
          href="https://en.wikipedia.org/wiki/Mobile_phone"
          target="_blank" rel="nofollow" title="Mobile phone">mobile
          phones</a> with <a
          href="https://en.wikipedia.org/wiki/Pocket_PC" target="_blank"
          rel="nofollow" title="Pocket PC">Pocket PC</a> rather than the
          <a href="https://en.wikipedia.org/wiki/Palm_os"
          target="_blank" rel="nofollow" title="Palm OS">Palm OS</a>.
          On November 2005, <a
          href="https://en.wikipedia.org/wiki/Palm_os" target="_blank"
          rel="nofollow" title="PalmSource">PalmSource</a> (the <a
          href="https://en.wikipedia.org/wiki/Software_developer"
          target="_blank" rel="nofollow" title="Software
          developer">developer</a> of the <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">operating systems</a>) was acquired by <a
          href="http://www.access-us-inc.com/" target="_blank"
          rel="nofollow" title="Access">Access</a>.</p>

          <p class="lead" id="2006">On June 2006, <a
          href="http://www.access-us-inc.com/" target="_blank"
          rel="nofollow" title="Access">Access</a> decided to drop <a
          href="https://en.wikipedia.org/wiki/Palm_os"
          target="_blank" rel="nofollow" title="Palm OS">Palm OS</a> in
          favor of <a
          href="http://www.access.co.jp/english/press/060214.html"
          target="_blank" rel="nofollow" title="press release for ACCESS
          Linux Platform">ACCESS Linux Platform</a> (<a
          href="/linux.php" target="_self" rel="dofollow"
          title="Linux">Linux</a>).</p>

          <p class="lead" id="2010">In April 2010, <a
          href="https://en.wikipedia.org/wiki/Hewlett-Packard"
          target="_blank" rel="nofollow"
          title="Hewlett-Packard">Hewlett-Packard</a> acquired <a
          href="https://en.wikipedia.org/wiki/Palm,_Inc."
          target="_blank" rel="nofollow" title="Palm Inc.">Palm</a> and
          all related properties.  The new product release is <a
          href="https://en.wikipedia.org/wiki/WebOS"
          target="_blank" rel="nofollow" title="webOS">webOS</a>, which
          based on <a href="/linux.php" target="_self" rel="dofollow"
          title="Linux">Linux</a> marking an end to <a
          href="https://en.wikipedia.org/wiki/Palm_os" target="_blank"
          rel="nofollow" title="Palm OS">Palm OS</a>.</p>

          <p class="lead">Nonetheless, <a
          href="https://en.wikipedia.org/wiki/Palm_os" target="_blank"
          rel="nofollow" title="Palm OS">Palm OS</a> is an <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">operating systems</a> for hand-held <a
          href="https://en.wikipedia.org/wiki/Computer"
          target="_blank" rel="nofollow" title="Computer">computers</a>
          that is very easy to use and to share <a
          href="https://en.wikipedia.org/wiki/Data" target="_blank"
          rel="nofollow" title="Data">data</a> with any other <a
          href="https://en.wikipedia.org/wiki/Personal_computer"
          target="_blank" rel="nofollow" title="Personal
          computer">PC</a>, compared to the <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">installation</a> procedure of the synchronization
          <a href="https://en.wikipedia.org/wiki/Utility_software"
          target="_blank" rel="nofollow" title="Utility
          software">utility</a> for <a
          href="https://en.wikipedia.org/wiki/Windows_Mobile"
          target="_blank" rel="nofollow" title="Windows Mobile">Windows
          Mobile</a> (formerly known as <a
          href="https://en.wikipedia.org/wiki/Windows_Mobile"
          target="_blank" rel="nofollow" title="Windows Mobile">Pocket
          Mobile</a>, before that as <a
          href="https://en.wikipedia.org/wiki/Pocket_PC" target="_blank"
          rel="nofollow" title="Pocket PC">Pocket PC</a> and before that
          as <a href="https://en.wikipedia.org/wiki/Windows_CE"
          target="_blank" rel="nofollow" title="Windows CE">Windows
          CE</a>).</p>

          <p class="lead"><a
          href="https://en.wikipedia.org/wiki/Palm_os" target="_blank"
          rel="nofollow" title="Palm OS">Palm OS</a> was originally <a
          href="https://en.wikipedia.org/wiki/Software_development_process"
          target="_blank" rel="nofollow" title="Software development
          process">developed</a> by a division of the <a
          href="https://en.wikipedia.org/wiki/Tandy_Corporation"
          target="_blank" rel="nofollow" title="Tandy Corporation">Tandy
          Corporation</a> (now <a
          href="https://en.wikipedia.org/wiki/RadioShack"
          target="_blank" rel="nofollow"
          title="RadioShack">RadioShack</a>), which was an <a
          href="https://en.wikipedia.org/wiki/IBM" target="_blank"
          rel="nofollow" title="IBM">IBM</a> <a
          href="https://en.wikipedia.org/wiki/Clone_%28computing%29"
          target="_blank" rel="nofollow" title="Clone
          (computing)">clone</a> with a touch sensitive <a
          href="https://en.wikipedia.org/wiki/Display_device"
          target="_blank" rel="nofollow" title="Display
          device">display</a> in 1989.  The division of <a
          href="https://en.wikipedia.org/wiki/Tandy_Corporation"
          target="_blank" rel="nofollow" title="Tandy Corporation">Tandy
          Corporation</a> became <a
          href="https://en.wikipedia.org/wiki/Palm,_Inc."
          target="_blank" rel="nofollow" title="Palm Inc.">Palm
          Computing</a> in 1992.  Around 1995 <a
          href="https://en.wikipedia.org/wiki/USRobotics"
          target="_blank" rel="nofollow"
          title="USRobotics">USRobotics</a> acquired <a
          href="https://en.wikipedia.org/wiki/Palm,_Inc."
          target="_blank" rel="nofollow" title="Palm Inc.">Palm</a> as a
          subsidiary, which was later acquired by <a
          href="https://en.wikipedia.org/wiki/3Com" target="_blank"
          rel="nofollow" title="3Com">3Com</a>.  <a
          href="https://en.wikipedia.org/wiki/Palm,_Inc."
          target="_blank" rel="nofollow" title="Palm Inc.">Palm</a>
          became an independent company again years later and some of
          the <a href="https://en.wikipedia.org/wiki/Software_developer"
          target="_blank" rel="nofollow" title="Software
          developer">developers</a> formed <a
          href="https://en.wikipedia.org/wiki/Handspring_%28company%29"
          target="_blank" rel="nofollow" title="Handspring
          (company)">Handspring</a>.  <a
          href="https://en.wikipedia.org/wiki/Palm,_Inc."
          target="_blank" rel="nofollow" title="Palm Inc.">Palm</a> and
          <a
          href="https://en.wikipedia.org/wiki/Handspring_%28company%29"
          target="_blank" rel="nofollow" title="Handspring
          (company)">Handspring</a> were part of the original <a
          href="https://en.wikipedia.org/wiki/Software_development_process"
          target="_blank" rel="nofollow" title="Software development
          process">developing</a> team behind the <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">operating systems</a>.  Both <a
          href="https://en.wikipedia.org/wiki/Palm,_Inc."
          target="_blank" rel="nofollow" title="Palm Inc.">Palm</a> and
          <a
          href="https://en.wikipedia.org/wiki/Handspring_%28company%29"
          target="_blank" rel="nofollow" title="Handspring
          (company)">Handspring</a> promoted the <a
          href="https://en.wikipedia.org/wiki/Software_development_process"
          target="_blank" rel="nofollow" title="Software development
          process">development</a> and marketing of <a
          href="https://en.wikipedia.org/wiki/Software"
          target="_blank" rel="nofollow" title="Software">software</a>
          and <a
          href="https://en.wikipedia.org/wiki/Compatibility_layer"
          target="_blank" rel="nofollow" title="Compatibility
          layer">compatible</a> <a
          href="https://en.wikipedia.org/wiki/Computer_hardware"
          target="_blank" rel="nofollow" title="Hardware">hardware</a>.
          To make the relation of that these two companies have with
          each other more confusing, <a
          href="https://en.wikipedia.org/wiki/Palm,_Inc."
          target="_blank" rel="nofollow" title="Palm Inc.">Palm</a>
          acquired <a
          href="https://en.wikipedia.org/wiki/Handspring_%28company%29"
          target="_blank" rel="nofollow" title="Handspring
          (company)">Handspring</a> in 2003 giving the <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">operating systems</a> new life under the name <a
          href="https://en.wikipedia.org/wiki/Palm,_Inc."
          target="_blank" rel="nofollow" title="Palm Inc.">PalmOne</a>,
          which was later dropped in favor of the original name, <a
          href="https://en.wikipedia.org/wiki/Palm,_Inc."
          target="_blank" rel="nofollow" title="Palm Inc.">Palm</a>.</p>

          <p class="lead">With the changes in the <a
          href="https://en.wikipedia.org/wiki/Palm_os" target="_blank"
          rel="nofollow" title="Palm OS">Palm OS 4 and 5</a>, some
          vendors were forced to change <a
          href="https://en.wikipedia.org/wiki/Computer_hardware"
