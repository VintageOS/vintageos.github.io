          <h1 id="main">What is an operating system (OS)?</h1>

          <p class="lead">In the simplest definition, an <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">operating system</a> (<a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">OS</a>) is a set of instructions (<a
          href="https://en.wikipedia.org/wiki/Software" target="_blank"
          rel="nofollow" title="Software">software</a>) that controls
          some <a href="https://en.wikipedia.org/wiki/Computer_hardware"
          target="_blank" rel="nofollow" title="Hardware">hardware</a>.
          These instructions are layered over the <a
          href="https://en.wikipedia.org/wiki/Kernel_%28computing%29"
          target="_blank" rel="nofollow" title="Kernel
          (computing)">kernel</a> accessing the <a
          href="https://en.wikipedia.org/wiki/Computer_hardware"
          target="_blank" rel="nofollow" title="Hardware">hardware</a>
          directly and the <a
          href="https://en.wikipedia.org/wiki/Command_line_interface"
          target="_blank" rel="nofollow" title="Command line
          interface">command line interface</a> (<a
          href="https://en.wikipedia.org/wiki/Command_line_interface"
          target="_blank" rel="nofollow" title="Command line
          interface">CLI</a>) or <a
          href="https://en.wikipedia.org/wiki/Graphical_user_interface"
          target="_blank" rel="nofollow" title="Graphical user
          interface">graphical user interface</a> (<a
          href="https://en.wikipedia.org/wiki/Graphical_user_interface"
          target="_blank" rel="nofollow" title="Graphical user
          interface">GUI</a>) for the <a
          href="https://en.wikipedia.org/wiki/User_%28computing%29"
          target="_blank" rel="nofollow" title="User
          (computing)">user</a> (normally a human being) to interact
          with it.  These instructions are written (<a
          href="https://en.wikipedia.org/wiki/Source_code"
          target="_blank" rel="nofollow" title="Source code">coded</a>)
          using in different <a
          href="https://en.wikipedia.org/wiki/Programming_language"
          target="_blank" rel="nofollow" title="Programming
          language">languages</a> from <a
          href="https://en.wikipedia.org/wiki/Low-level_programming_language"
          target="_blank" rel="nofollow" title="Low-level programming
          languages">low-level</a> <a
          href="https://en.wikipedia.org/wiki/Programming_language"
          target="_blank" rel="nofollow" title="Programming
          language">languages</a> (like <a
          href="https://en.wikipedia.org/wiki/Machine_code"
          target="_blank" rel="nofollow" title="Machine code">machine
          language</a> and <a
          href="https://en.wikipedia.org/wiki/Assembler_%28computing%29"
          target="_blank" rel="nofollow" title="Assembler
          (computing)">assembler</a>) to <a
          href="https://en.wikipedia.org/wiki/High-level_programming_language"
          target="_blank" rel="nofollow" title="High-level programming
          languages">high-level</a> <a
          href="https://en.wikipedia.org/wiki/Programming_language"
          target="_blank" rel="nofollow" title="Programming
          language">languages</a> (like <a
          href="https://en.wikipedia.org/wiki/C%2B%2B" target="_blank"
          rel="nofollow" title="C++">C++</a>).</p>

          <p class="lead"><a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">Operating systems</a> have evolved from their early
          days and surprisingly most modern <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">operating systems</a> were made a good time ago like
          <a href="/mvs.php" target="_self" rel="dofollow"
          title="Multiple Virtual Storage">MVS</a> in 1964, <a
          href="/unix.php" target="_self" rel="dofollow"
          title="Unix">Unix</a> in 1969, <a href="/bsd.php"
          target="_self" rel="dofollow" title="Berkeley Software
          Distribution">BSD</a> in 1977, <a href="/dos.php"
          target="_self" rel="dofollow" title="Disk Operating
          System">DOS</a> in 1980, <a href="/macos.php" target="_self"
          rel="dofollow" title="macOS">Macintosh</a> (or <a
          href="/macos.php" target="_self" rel="dofollow"
          title="macOS">Mac</a>) in 1984, <a href="/windows.php"
          target="_self" rel="dofollow" title="Microsoft
          Windows">Windows</a> in 1985 and <a href="/linux.php"
          target="_self" rel="dofollow" title="Linux">Linux</a> in
          1991.</p>

