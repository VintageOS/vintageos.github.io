          <p class="lead" id="1994"><a href="/frn2000.php"
          target="_self" rel="author" title="FRN2000 for VintageOS
          v133C971">My</a> experiences with this system are limited to
          <a href="https://en.wikipedia.org/wiki/OS%2F2" target="_blank"
          rel="nofollow" title="OS/2">OS/2 Warp 3</a> (1994).  <a
          href="https://en.wikipedia.org/wiki/OS%2F2" target="_blank"
          rel="nofollow" title="OS/2">OS/2</a> has the same feeling as
          <a href="/windows.php" target="_self" rel="dofollow"
          title="Microsoft Windows">Windows 3.11</a> or <a
          href="/linux.php" target="_self" rel="dofollow"
          title="Linux">Linux 1.3</a> with a chunky <a
          href="https://en.wikipedia.org/wiki/Taskbar" target="_blank"
          rel="nofollow" title="Taskbar">taskbar</a> floating on the
          screen.  <a href="https://en.wikipedia.org/wiki/OS%2F2"
          target="_blank" rel="nofollow" title="OS/2">OS/2 Warp 3</a>
          came in two <em>flavors</em> &#151; blue and red spines.  The
          red spine version fully supported installing <a
          href="/windows.php" target="_self" rel="dofollow"
          title="Microsoft Windows">Windows 3.11</a> under it and the
          blue spine offering no support.</p>

          <p class="lead" id="2000">By 2000, the <a
          href="https://en.wikipedia.org/wiki/Software_development_process"
          target="_blank" rel="nofollow" title="Software development
          process">development</a> of this project was picked up by
          Serenity Systems International under the name <a
          href="https://en.wikipedia.org/wiki/EComStation"
          target="_blank" rel="nofollow"
          title="eComStation">eComStation</a>, which kept its <a
          href="https://en.wikipedia.org/wiki/Graphical_user_interface"
          target="_blank" rel="nofollow" title="Graphical user
          interface">GUI</a> similar to the <a
          href="https://en.wikipedia.org/wiki/X_Window_System"
          target="_blank" rel="nofollow" title="X Windows System">X
          Windows System</a>.</p>

          <p class="lead" id="2005">On December 2005, <a
          href="https://en.wikipedia.org/wiki/IBM" target="_blank"
          rel="nofollow" title="IBM">IBM</a> discontinued the <a
          href="https://en.wikipedia.org/wiki/OS%2F2" target="_blank"
          rel="nofollow" title="OS/2">OS/2</a> project.  Many people
          think that the lack of interest to this <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating system">OS</a>
          is the result of weak North American promotion.  <a
          href="https://en.wikipedia.org/wiki/OS%2F2" target="_blank"
          rel="nofollow" title="OS/2">OS/2</a> is more popular in Europe
          than in the US.  Many of the sites that <a href="/frn2000.php"
          target="_self" rel="dofollow" title="FRN2000 for VintageOS
          v133C971">I</a> have visited are in Russia and/or Germany.</p>

          <p class="lead" id="2015">On November 2015, Arca Noae
          announced project <em>Blue Lion</em> as a <a
          href="https://en.wikipedia.org/wiki/Software_distribution"
          target="_blank" rel="nofollow" title="Software
          distribution">distribution</a> based on <a
          href="https://en.wikipedia.org/wiki/OS%2F2" target="_blank"
          rel="nofollow" title="OS/2">OS/2 Warp 4</a> under license from
          <a href="https://en.wikipedia.org/wiki/IBM" target="_blank"
          rel="nofollow" title="IBM">IBM</a> &#151; not based on <a
          href="https://en.wikipedia.org/wiki/EComStation"
          target="_blank" rel="nofollow"
          title="eComStation">eComStation</a>.</p>

          <p class="lead" id="2017">In April 2017, Blue Lion was
          released as <a href="https://en.wikipedia.org/wiki/OS%2F2"
          target="_blank" rel="nofollow" title="OS/2">ArcaOS 5.0</a> (<a
          href="https://en.wikipedia.org/wiki/OS%2F2" target="_blank"
          rel="nofollow" title="OS/2">OS/2 5</a>).  There is no clear
          explanation what is going to happen to license and production
          of <a href="https://en.wikipedia.org/wiki/EComStation"
          target="_blank" rel="nofollow"
          title="eComStation">eComStation</a> with the licensing and
          release of <a href="https://en.wikipedia.org/wiki/OS%2F2"
          target="_blank" rel="nofollow" title="OS/2">ArcaOS 5.0</a>.
          Perhaps <a href="https://en.wikipedia.org/wiki/OS%2F2"
          target="_blank" rel="nofollow" title="OS/2">OS/2</a> will
          become the next <a href="/linux.php" target="_self"
          rel="dofollow" title="Linux">Linux</a>, where multiple
          companies and/or organizations can release custom <a
          href="https://en.wikipedia.org/wiki/Software_distribution"
