          <p class="lead" id="1969"><a
          href="https://en.wikipedia.org/wiki/File:Ken_n_dennis.jpg"
          target="_blank" rel="nofollow"><img
          src="/img/220px-Ken_n_dennis.jpg" height="143" width="220"
          border="0" align="right" alt="Kenneth Thompson (left) with
          Dennis Ritchie (right)" title="Kenneth Thompson (left) with
          Dennis Ritchie (right)" /></a><a
          href="https://en.wikipedia.org/wiki/Unix" target="_blank"
          rel="nofollow" title="Unix">Unix</a> is one of the oldest <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">OS</a>, originally written (<a
          href="https://en.wikipedia.org/wiki/Source_code"
          target="_blank" rel="nofollow" title="Source code">coded</a>)
          in <a
          href="https://en.wikipedia.org/wiki/Assembler_%28computing%29"
          target="_blank" rel="nofollow" title="Assembler
          (computing)">assembler</a> in 1969 by <a
          href="https://en.wikipedia.org/wiki/Dennis_Ritchie"
          target="_blank" rel="nofollow" title="Dennis Ritchie">Dennis
          Ritchie</a> and <a
          href="https://en.wikipedia.org/wiki/Ken_Thompson"
          target="_blank" rel="nofollow" title="Ken Thompson">Ken
          Thompson</a> at <a
          href="https://en.wikipedia.org/wiki/Bell_Labs" target="_blank"
          rel="nofollow" title="Bell Labs">Bell Labs</a>.  Later <a
          href="https://en.wikipedia.org/wiki/Unix" target="_blank"
          rel="nofollow" title="Unix">Unix</a> was written (<a
          href="https://en.wikipedia.org/wiki/Source_code"
          target="_blank" rel="nofollow" title="Source code">coded</a>)
          again in <a
          href="https://en.wikipedia.org/wiki/C_%28programming_language%29"
          target="_blank" rel="nofollow" title="C (programming
          language)">C</a> (its native <a
          href="https://en.wikipedia.org/wiki/Programming_language"
          target="_blank" rel="nofollow" title="Programming
          language">language</a>) for <a
          href="https://en.wikipedia.org/wiki/Software_portability"
          target="_blank" rel="nofollow" title="Software
          portability">portability</a> in 1972.  <a
          href="https://en.wikipedia.org/wiki/Unix" target="_blank"
          rel="nofollow" title="Unix">Unix</a> is the most <a
          href="https://en.wikipedia.org/wiki/Portable_application"
          target="_blank" rel="nofollow" title="Portable
          application">portable</a> and reliable <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">OS</a>.</p>

          <p class="lead">By the way, <a
          href="https://en.wikipedia.org/wiki/Unix" target="_blank"
          rel="nofollow" title="Unix">UNIX&#169;</a> is not the same as
          <a href="https://en.wikipedia.org/wiki/Unix" target="_blank"
          rel="nofollow" title="Unix">Unix</a>.  <a
          href="https://en.wikipedia.org/wiki/Unix" target="_blank"
          rel="nofollow" title="Unix">UNIX&#169;</a> (all capital
          letters and often presented with the <a
          href="https://en.wikipedia.org/wiki/Copyright" target="_blank"
          rel="nofollow" title="Copyright">copyright</a> symbol) is a <a
          href="https://en.wikipedia.org/wiki/Trademark"
          target="_blank" rel="nofollow" title="Trademark">trademark</a>
          while <a href="https://en.wikipedia.org/wiki/Unix"
          target="_blank" rel="nofollow" title="Unix">Unix</a> (a proper
          name) is a <a href="https://en.wikipedia.org/wiki/Philosophy"
          target="_blank" rel="nofollow"
          title="Philosophy">philosophy</a>.  Considering the latter, <a
          href="/bsd.php" target="_self" rel="dofollow" title="Berkeley
          Software Distribution">BSD</a> and <a href="/linux.php"
          target="_self" rel="dofollow" title="Linux">Linux</a> are not
          <a href="https://en.wikipedia.org/wiki/Unix" target="_blank"
          rel="nofollow" title="Unix">UNIX&#169;</a>.  They are <a
          href="https://en.wikipedia.org/wiki/Unix" target="_blank"
          rel="nofollow" title="Unix">UNIX&#169;</a> <a
          href="https://en.wikipedia.org/wiki/Clone_%28computing%29"
          target="_blank" rel="nofollow" title="Clone
          (computing)">clones</a>, but nonetheless they are considered
          <a href="https://en.wikipedia.org/wiki/Unix" target="_blank"
          rel="nofollow" title="Unix">Unix</a>.  If you are confused,
          you are not alone.</p>

          <p class="lead">Several vendors have registered versions of <a
          href="https://en.wikipedia.org/wiki/Unix" target="_blank"
          rel="nofollow" title="Unix">UNIX&#169;</a>.  They are <a
          href="https://en.wikipedia.org/wiki/Tru64_UNIX"
          target="_blank" rel="nofollow" title="Tru64 Unix">Tru64
          Unix</a>, <a href="https://en.wikipedia.org/wiki/IBM_AIX"
          target="_blank" rel="nofollow" title="IBM AIX">IBM AIX</a>, <a
          href="https://en.wikipedia.org/wiki/UnixWare" target="_blank"
          rel="nofollow" title="UnixWare">UnixWare</a>, <a
          href="https://en.wikipedia.org/wiki/IRIX" target="_blank"
          rel="nofollow" title="IRIX">IRIX</a> and <a
          href="https://en.wikipedia.org/wiki/Solaris_%28operating_system%29"
          target="_blank" rel="nofollow" title="Sun Solaris">Sun
          Solaris</a>.</p>

          <p class="lead">At the same time, there are several <a
          href="https://en.wikipedia.org/wiki/Unix" target="_blank"
          rel="nofollow" title="Unix">Unix</a> <a
          href="https://en.wikipedia.org/wiki/Clone_%28computing%29"
          target="_blank" rel="nofollow" title="Clone
          (computing)">clones</a> like <a href="/bsd.php" target="_self"
          rel="dofollow" title="Berkeley Software Distribution">BSD</a>
          and <a href="/linux.php" target="_self" rel="dofollow"
          title="Linux">Linux</a>.  For those who work with <a
          href="/windows.php" target="_self" rel="dofollow"
          title="Microsoft Windows">Windows</a> and do not want to
          handle a separate <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating system">OS</a>
          <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">installation</a>, <a href="/cygwin.php"
          target="_self" rel="dofollow" title="Cygwin">Cygwin</a> is
          handy with its <a href="https://en.wikipedia.org/wiki/Unix"
          target="_blank" rel="nofollow" title="Unix">Unix</a>-like (<a
          href="/linux.php" target="_self" rel="dofollow"
          title="Linux">Linux</a>-like) <a
          href="https://en.wikipedia.org/wiki/Runtime_system"
          target="_blank" rel="nofollow" title="Runtime
          system">environment</a>.</p>

          <p class="lead">About 90% of the <a
          href="https://en.wikipedia.org/wiki/Internet"
          target="_blank" rel="nofollow" title="Internet">internet</a>
          relies on a variation of <a
          href="https://en.wikipedia.org/wiki/Unix" target="_blank"
          rel="nofollow" title="Unix">Unix</a> (normally either <a
