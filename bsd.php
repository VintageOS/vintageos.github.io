          <h1 id="main">What is BSD?</h1>

          <p class="lead">There is a close relation between <a
          href="https://en.wikipedia.org/wiki/Berkeley_Software_Distribution"
          target="_blank" rel="nofollow" title="Berkeley Software
          Distribution">BSD</a> and <a href="/linux.php" target="_self"
          rel="dofollow" title="Linux">Linux</a>.  They are both <a
          href="/unix.php" target="_self" rel="dofollow"
          title="UNIX&#169;">UNIX&#169;</a> <a
          href="https://en.wikipedia.org/wiki/Clone_%28computing%29"
          target="_blank" rel="nofollow" title="Clone
          (computing)">clones</a>, sort of like distant cousins.  <a
          href="https://en.wikipedia.org/wiki/Berkeley_Software_Distribution"
          target="_blank" rel="nofollow" title="Berkeley Software
          Distribution">BSD</a> stands for <a
          href="https://en.wikipedia.org/wiki/Berkeley_Software_Distribution"
          Distribution</a> because it was <a
          href="https://en.wikipedia.org/wiki/Software_development_process"
          target="_blank" rel="nofollow" title="Software development
          process">developed</a> at <a
          href="https://en.wikipedia.org/wiki/University_of_California,_Berkeley"
          target="_blank" rel="nofollow" title="University of
          California, Berkley">University of California at Berkley</a>
          in 1977.  Since 1970's, the <a
          href="https://en.wikipedia.org/wiki/Source_code"
          target="_blank" rel="nofollow" title="Source code">source
          code</a> for <a href="/unix.php" target="_self" rel="dofollow"
          title="Unix">Unix</a> was included with the releases to allow
          further <a
          href="https://en.wikipedia.org/wiki/Software_development_process"
          target="_blank" rel="nofollow" title="Software development
          process">development</a>.  The changes done at <a
          href="https://en.wikipedia.org/wiki/University_of_California,_Berkeley"
          target="_blank" rel="nofollow" title="University of
          California, Berkley">Berkley</a> became known as <a
          href="https://en.wikipedia.org/wiki/Berkeley_Software_Distribution"
          target="_blank" rel="nofollow" title="Berkeley Software
          Distribution">BSD</a>.  Although every version and adaptation
          of <a href="/unix.php" target="_self" rel="dofollow"
          title="Unix">Unix</a> is important, the 1984 release of <a
          href="https://en.wikipedia.org/wiki/Berkeley_Software_Distribution"
          target="_blank" rel="nofollow" title="Berkeley Software
          Distribution">BSD</a> known as <a
          href="https://en.wikipedia.org/wiki/Berkeley_Software_Distribution#4.4BSD_and_descendants"
          target="_blank" rel="nofollow" title="Berkeley Software
          Distribution, 4.4BSD and descendants">4.2BSD</a> became
          popular and commonly used within universities.</p>

          <p class="lead" id="386BSD">In 1985, <a
          href="https://en.wikipedia.org/wiki/William_Jolitz"
          target="_blank" rel="nofollow" title="William Jolitz">William
          Jolitz</a> and <a
          href="https://en.wikipedia.org/wiki/Lynne_Jolitz"
          target="_blank" rel="nofollow" title="Lynne Jolitz">Lynne
          Jolitz</a> adapted the <a
          href="https://en.wikipedia.org/wiki/Source_code"
          target="_blank" rel="nofollow" title="Source code">source
          code</a> of <a
          href="https://en.wikipedia.org/wiki/Berkeley_Software_Distribution"
          target="_blank" rel="nofollow" title="Berkeley Software
          Distribution">BSD</a> to work on the <a
          href="https://en.wikipedia.org/wiki/Intel_80386"
          target="_blank" rel="nofollow" title="Intel 80386">i386</a> <a
          href="https://en.wikipedia.org/wiki/Microprocessor"
          target="_blank" rel="nofollow"
          title="Microprocessor">microprocessor</a> under the name <a
          href="https://en.wikipedia.org/wiki/386BSD" target="_blank"
          rel="nofollow" title="386BSD">386BSD</a>.</p>

          <p class="lead" id="MacBSD">In 1991, Brad Grantham, Lawrence
          Kesteloot and Chris Caputo <a
          href="https://en.wikipedia.org/wiki/Porting" target="_blank"
          rel="nofollow" title="Porting">ported</a> <a
          href="https://en.wikipedia.org/wiki/386BSD" target="_blank"
          rel="nofollow" title="386BSD">386BSD</a> to <a
          href="/macos.php" target="_self" rel="dofollow"
          title="macOS">Mac</a> under the name <a
          href="https://en.wikipedia.org/wiki/NetBSDPorts%2Fmac68k"
          target="_blank" rel="nofollow"
          title="NetBSD/mac68k">MacBSD</a>.  In 1992, <a
          href="https://en.wikipedia.org/wiki/Berkeley_Software_Distribution"
          target="_blank" rel="nofollow" title="Berkeley Software
          Distribution">BSD</a> was running on <a
          href="https://en.wikipedia.org/wiki/Macintosh_II"
          target="_blank" rel="nofollow" title="Macintosh II">Mac II</a>
          <a href="https://en.wikipedia.org/wiki/Computer"
          target="_blank" rel="nofollow"
          title="Computer">systems</a>.</p>

          <p class="lead" id="NetBSD">In May 1993, <a
          href="https://en.wikipedia.org/wiki/NetBSD" target="_blank"
          rel="nofollow" title="NetBSD">NetBSD 0.8</a> was first
          released.  <a href="https://en.wikipedia.org/wiki/NetBSD"
          target="_blank" rel="nofollow" title="NetBSD">NetBSD</a> was
          the first <a href="https://en.wikipedia.org/wiki/Royalty-free"
          target="_blank" rel="nofollow"
          title="Royalty-free">royalty-free</a> <a href="/unix.php"
          target="_self" rel="dofollow" title="Unix">Unix</a>-like <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">OS</a>.  Around the same time, Allen Briggs and
          Michael Finch started to merge <a
          href="https://en.wikipedia.org/wiki/NetBSD" target="_blank"
          rel="nofollow" title="NetBSD">NetBSD 0.8</a> and <a
          href="https://en.wikipedia.org/wiki/NetBSDPorts%2Fmac68k"
          target="_blank" rel="nofollow"
          title="NetBSD/mac68k">MacBSD</a> to avoid possible feuds.  By
          the time <a href="https://en.wikipedia.org/wiki/NetBSD"
          target="_blank" rel="nofollow" title="NetBSD">NetBSD 1.0</a>
          came around, the <a
          href="https://en.wikipedia.org/wiki/NetBSDPorts%2Fmac68k"
          target="_blank" rel="nofollow"
          title="NetBSD/mac68k">NetBSD/mac68k</a> project was
          established.  See <a href="/bsd.php#NetBSD_distros"
          target="_self" rel="dofollow" title="Berkeley Software
          Distribution &#187 List of Distributions Based on NetBSD
          (1993)">list of distributions</a> based on <a
          href="https://en.wikipedia.org/wiki/NetBSD" target="_blank"
          rel="nofollow" title="NetBSD">NetBSD</a>.</a>

          <p class="lead" id="FreeBSD">In November of 1993, Nate
          Williams, Rod Grimes and <a
          href="https://en.wikipedia.org/wiki/Jordan_Hubbard"
          target="_blank" rel="nofollow" title="Jordan Hubbard">Jordan
          Hubbard</a> released <a
          href="https://en.wikipedia.org/wiki/FreeBSD" target="_blank"
          rel="nofollow" title="FreeBSD">FreeBSD 1.0</a> for <a
          href="https://en.wikipedia.org/wiki/X86" target="_blank"
          rel="nofollow" title="x86">x86</a> <a
          href="https://en.wikipedia.org/wiki/Microprocessor"
          target="_blank" rel="nofollow"
          title="Microprocessor">microprocessors</a> based on <a
          href="https://en.wikipedia.org/wiki/Berkeley_Software_Distribution"
          target="_blank" rel="nofollow" title="Berkeley Software
          Distribution">4.4BSD-Lite</a> (1995) with components of the
          Unofficial <a href="https://en.wikipedia.org/wiki/386BSD"
          target="_blank" rel="nofollow" title="386BSD">386BSD</a> <a
          href="https://en.wikipedia.org/wiki/Patch_%28computing%29"
          target="_blank" rel="nofollow" title="Patch
          (computing)">Patchkit</a>, which they had <a
          href="https://en.wikipedia.org/wiki/Software_development_process"
          target="_blank" rel="nofollow" title="Software development
          process">developed</a> for <a
          href="https://en.wikipedia.org/wiki/William_Jolitz"
          target="_blank" rel="nofollow" title="William Jolitz">William
          Jolitz</a>'s <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">OS</a>.  <a
          href="https://en.wikipedia.org/wiki/Jordan_Hubbard"
          target="_blank" rel="nofollow" title="Jordan Hubbard">Jordan
          Hubbard</a> is also credited for <a
          href="https://en.wikipedia.org/wiki/FreeBSD_Ports"
          target="_blank" rel="nofollow" title="FreeBSD Ports">FreeBSD
          Ports</a>, an easy way to <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">install</a> <a
          href="https://en.wikipedia.org/wiki/Computer_program"
          target="_blank" rel="nofollow" title="Computer
          programming">programs</a> via the <tt>Makefile</tt> <a
          href="https://en.wikipedia.org/wiki/Macro_%28computer_science%29"
          target="_blank" rel="nofollow" title="Macro (computer
          science)">macro</a>.  See <a href="/bsd.php#FreeBSD_distros"
          target="_self" rel="dofollow" title="Berkeley Software
          Distribution &#187 List of Distributions Based on FreeBSD
          (1993)">list of distributions</a> based on <a
          href="https://en.wikipedia.org/wiki/FreeBSD" target="_blank"
          rel="nofollow" title="FreeBSD">FreeBSD</a>.</p>

          <p class="lead" id name="OpenBSD">In October 1995,
          <a href="https://en.wikipedia.org/wiki/Theo_de_Raadt"
          target="_blank" rel="nofollow" title="Theo de Raadt">Theo de
          Raadt</a> (a former core team and co-founder of <a
          href="https://en.wikipedia.org/wiki/NetBSD" target="_blank"
          rel="nofollow" title="NetBSD">NetBSD</a>, who asked to leave
          the organization) first released <a
          href="https://en.wikipedia.org/wiki/OpenBSD" target="_blank"
          rel="nofollow" title="OpenBSD">OpenBSD 2.0</a> as a <a
          href="https://en.wikipedia.org/wiki/Fork_%28software_development%29"
          target="_blank" rel="nofollow" title="Fork (software
          development)">fork</a> of <a
          href="https://en.wikipedia.org/wiki/NetBSD" target="_blank"
          rel="nofollow" title="NetBSD">NetBSD</a>.  See <a
          href="/bsd.php#OpenBSD_distros" target="_self" rel="dofollow"
          title="Berkeley Software Distribution &#187 List of
          Distributions Based on OpenBSD (1995)">list of
          distributions</a> based on <a
          href="https://en.wikipedia.org/wiki/OpenBSD" target="_blank"
          rel="nofollow" title="OpenBSD">OpenBSD</a>.</p>

          <p class="lead"><a id="Darwin">In November 2000, <a
          href="https://en.wikipedia.org/wiki/Apple_Inc."
          target="_blank" rel="nofollow" title="Apple Inc.">Apple</a>
          used <a
