          <a
          href="https://en.wikipedia.org/wiki/Handspring_%28company%29"
          target="_blank" rel="nofollow" title="Handspring
          (company)">Handspring</a>, which dropped <a
          href="https://en.wikipedia.org/wiki/Springboard_Expansion_Slot"
          target="_blank" rel="nofollow" title="Springboard Expansion
          Slot">Springboard</a> modules from their newer models.  These
          modules were interchangeable units that gave extra functions
          to any <a
          href="https://en.wikipedia.org/wiki/Handspring_%28company%29"
          target="_blank" rel="nofollow" title="Handspring
          (company)">Handspring</a> unit like dictionaries, <a
          href="https://en.wikipedia.org/wiki/Encyclopedia"
          target="_blank" rel="nofollow"
          title="Encyclopedia">encyclopedias</a>, <a
          href="https://en.wikipedia.org/wiki/Audio" target="_blank"
          rel="nofollow" title="Audio">audio</a> recorders, <a
          href="https://en.wikipedia.org/wiki/Modem" target="_blank"
          rel="nofollow" title="Modem">modems</a>, <a
          href="https://en.wikipedia.org/wiki/Camera" target="_blank"
