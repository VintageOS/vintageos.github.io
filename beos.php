          <h2 id="Haiku">Haiku:</h2>

          <p class="lead"><a
          href="https://en.wikipedia.org/wiki/Haiku_%28operating_system%29"
          target="_blank" rel="nofollow" title="Haiku (operating
          system)">Haiku</a> was originally released as <a
          href="https://en.wikipedia.org/wiki/Haiku_OS" target="_blank"
          rel="nofollow" title="Haiku OS">OpenBeOS</a>.  This <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating system">OS</a>
          uses the <a
          href="https://en.wikipedia.org/wiki/Be_File_System"
          target="_blank" rel="nofollow" title="Be File System">Be File
          System</a> (<a
          href="https://en.wikipedia.org/wiki/Be_File_System"
          target="_blank" rel="nofollow" title="Be File System">BFS</a>)
          that works as a <a
          href="https://en.wikipedia.org/wiki/Metadata"
          target="_blank" rel="nofollow" title="Metadata">metadata</a>
          <a href="https://en.wikipedia.org/wiki/Database"
          target="_blank" rel="nofollow" title="Database">database</a>
          running on 400 <a href="https://en.wikipedia.org/wiki/Hertz"
          target="_blank" rel="nofollow" title="Hertz">MHz</a> <a
          href="https://en.wikipedia.org/wiki/Microprocessor"
          target="_blank" rel="nofollow"
          title="Microprocessor">microprocessor</a>, 128 <a
          href="https://en.wikipedia.org/wiki/Megabyte" target="_blank"
          rel="nofollow" title="Megabyte">MB</a> of <a
          href="https://en.wikipedia.org/wiki/Random-access_memory"
          target="_blank" rel="nofollow" title="Random-access
          memory">RAM</a> and 700 <a
          href="https://en.wikipedia.org/wiki/Megabyte" target="_blank"
          rel="nofollow" title="Megabyte">MB</a> of <a
          href="https://en.wikipedia.org/wiki/Hard_disk_drive"
          target="_blank" rel="nofollow" title="Hard disk drive">HDD</a>
          space.</p>

