          target="_blank" rel="nofollow"
          title="Chromebook">Chromebook</a> and <a href="/frn2000.php"
          target="_self" rel="author" title="FRN2000 for VintageOS
          v133C971">I</a> am more familiar with open standards.
          Unfortunately this, by no means, makes <a href="/frn2000.php"
          target="_self" rel="author" title="FRN2000 for VintageOS
          v133C971">me</a> an expert on any of these <a
