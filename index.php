          computing</a> (<a href="/web.php" target="_self"
          rel="dofollow" title="Web Platforms">web platforms</a>).</p>

          <p class="lead">At the same time, <a
          href="https://en.wikipedia.org/wiki/Computer" target="_blank"
          rel="nofollow" title="Computer">computers</a> (<a
          href="https://en.wikipedia.org/wiki/Microcomputer"
          target="_blank" rel="nofollow"
          title="Microcomputer">microcomputers</a>) have shrunk from
          huge <a href="https://en.wikipedia.org/wiki/Computer"
          target="_blank" rel="nofollow" title="Computer">machines</a>
          that would fit in a loft (like an <a
          href="https://en.wikipedia.org/wiki/IBM_360" target="_blank"
          rel="nofollow" title="IBM System/360">IBM System/360</a> <a
          href="https://en.wikipedia.org/wiki/Mainframe_computer"
          target="_blank" rel="nofollow" title="Mainframe
          computer">mainframe computers</a>) to <a
          href="https://en.wikipedia.org/wiki/Computer"
          target="_blank" rel="nofollow" title="Computer">machines</a>
          that fit in your pocket like any of today's <a
          href="https://en.wikipedia.org/wiki/Mobile_phone"
          target="_blank" rel="nofollow" title="Mobile phone">mobile
          phones</a>.  <a
          href="https://en.wikipedia.org/wiki/Computer" target="_blank"
          rel="nofollow" title="Computer">Computers</a> are used
          worldwide for various purposes from <a
          href="https://en.wikipedia.org/wiki/Web_browser"
          target="_blank" rel="nofollow" title="Web browser">web
          browsing</a> to going to outer space (<a
          href="https://en.wikipedia.org/wiki/NASA" target="_blank"
          rel="nofollow" title="NASA">NASA</a>).</p>

