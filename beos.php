          <h2 id="Installation">Installing BeOS (Haiku):</h2>

          <p class="lead">Note that <a href="/frn2000.php"
          target="_self" rel="author" title="FRN2000 for VintageOS
          v133C971">I</a> have only been able to <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">install</a> <a
          href="https://en.wikipedia.org/wiki/Haiku_%28operating_system%29"
          target="_blank" rel="nofollow" title="Haiku (operating
          system)">Haiku</a> on a <a
          href="https://en.wikipedia.org/wiki/VirtualBox#VirtualBox_Disk_Image"
          target="_blank" rel="nofollow" title="VirtualBox, Device
          virtualization">VDI</a>, not as a sole <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating system">OS</a>
          on a <a href="https://en.wikipedia.org/wiki/Hard_disk_drive"
          target="_blank" rel="nofollow" title="Hard disk drive">HDD</a>
          or <a href="https://en.wikipedia.org/wiki/Disk_partitioning"
          target="_blank" rel="nofollow" title="Disk
          partitioning">partition</a> using its <a
          href="https://en.wikipedia.org/wiki/ISO_image" target="_blank"
          rel="nofollow" title="ISO image">.ISO</a> <a
          href="https://en.wikipedia.org/wiki/Disk_image"
          target="_blank" rel="nofollow" title="Disk
          image">image</a>.</p>

          <p class="lead">Like most <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">operating systems</a>, the <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">installation</a> is handled by a <a
          href="https://en.wikipedia.org/wiki/Live_CD" target="_blank"
          rel="nofollow" title="Live CD">live disk</a>.  The process is
          straight forward.  It loads all necessary <a
          href="https://en.wikipedia.org/wiki/Device_driver"
          target="_blank" rel="nofollow" title="Device
          driver">drivers</a> including the <a
          href="https://en.wikipedia.org/wiki/IEEE_802.11"
          target="_blank" rel="nofollow" title="IEEE 802.11">802.11</a>
          adapter and there is barely any configuration needed.</p>

          <p class="lead">Although <a
          href="https://en.wikipedia.org/wiki/Haiku_%28operating_system%29"
          target="_blank" rel="nofollow" title="Haiku (operating
          system)">Haiku</a> recommends running its nightly builds, <a
          href="/frn2000.php" target="_self" rel="author" title="FRN2000
          for VintageOS v133C971">I</a> have found them <a
          href="https://en.wikipedia.org/wiki/Software_bug"
          target="_blank" rel="nofollow" title="Software bug">buggy</a>
          especially when running the default web browser &#151; <a
          href="https://en.wikipedia.org/wiki/WebPositive"
          target="_blank" rel="nofollow"
          title="WebPositive">WebPositive</a>, which did not even let me
          log in to my <a href="https://en.wikipedia.org/wiki/Google"
          target="_blank" rel="nofollow" title="Google">Google</a>
          account.</p>

