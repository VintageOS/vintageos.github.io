          <h2 id="Installation">Installing Palm OS:</h2>

          <p class="lead">Unless you are <a
          href="https://en.wikipedia.org/wiki/Upgrade" target="_blank"
          rel="nofollow" title="Upgrade">upgrading</a>, there is no
          access to <a href="https://en.wikipedia.org/wiki/Palm_os"
          target="_blank" rel="nofollow" title="Palm OS">Palm OS</a>.
          There is no way to <a
          href="https://en.wikipedia.org/wiki/FORMAT_%28command%29"
          target="_blank" rel="nofollow" title="FORMAT
          (command)">FORMAT</a> anything.  <a
          href="https://en.wikipedia.org/wiki/Palm_os" target="_blank"
          rel="nofollow" title="Palm OS">Palm OS</a> <a
          href="https://en.wikipedia.org/wiki/Computer_appliance"
          target="_blank" rel="nofollow" title="Computer
          appliance">devices</a> have no <a
          href="https://en.wikipedia.org/wiki/Hard_disk_drive"
          target="_blank" rel="nofollow" title="Hard disk
          drive">HDD</a>.  <a
          href="https://en.wikipedia.org/wiki/Palm_os" target="_blank"
          rel="nofollow" title="Palm OS">Palm OS</a> is hard-coded to a
          <a href="https://en.wikipedia.org/wiki/Read-only_memory"
          target="_blank" rel="nofollow" title="Read-only
          memory">ROM</a> <a
          href="https://en.wikipedia.org/wiki/Chipset"
          target="_blank" rel="nofollow" title="Chipset">chip</a>.  <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">Operating system</a> <a
          href="https://en.wikipedia.org/wiki/Upgrade" target="_blank"
          rel="nofollow" title="Upgrade">upgrades</a> are layered on
          the version of <a href="https://en.wikipedia.org/wiki/Palm_os"
          target="_blank" rel="nofollow" title="Palm OS">Palm OS</a>
          that the unit was shipped with.  Therefore they have to be <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">re-installed</a> if the unit is reset (powering the
          unit completely off).</p>

          <p class="lead">If there is no way <a
          href="https://en.wikipedia.org/wiki/User_%28computing%29"
          target="_blank" rel="nofollow" title="User
          (computing)">users</a> (<a
          href="https://en.wikipedia.org/wiki/Geek" target="_blank"
          rel="nofollow" title="Geek">geek</a> or clueless) can <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">install</a> the <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">OS</a>, you might wonder why <a href="/frn2000.php"
          target="_self" rel="author" title="FRN2000 for VintageOS
          v133C971">I</a> wrote this in the first place.  First,
          clueless <a href="https://en.wikipedia.org/wiki/End_user"
          target="_blank" rel="nofollow" title="End user">end users</a>
          are dangerous.  Some <a
          href="https://en.wikipedia.org/wiki/User_%28computing%29"
          target="_blank" rel="nofollow" title="User
          (computing)">users</a> understand that a <a
          href="https://en.wikipedia.org/wiki/Computer_virus"
          target="_blank" rel="nofollow" title="Computer
          virus">virus</a> can destroy a system, but they are unwilling
          to scan a <a
          href="https://en.wikipedia.org/wiki/Computer_file"
          target="_blank" rel="nofollow" title="Computer file">file</a>
          or <a href="https://en.wikipedia.org/wiki/Floppy"
          target="_blank" rel="nofollow" title="Floppy">floppy</a>.</p>

          <p class="lead">Second, any unit running any version of <a
          href="https://en.wikipedia.org/wiki/Palm_os" target="_blank"
          rel="nofollow" title="Palm OS">Palm OS</a> is the perfect way
          to introduce people to the <a
          href="https://en.wikipedia.org/wiki/Computer_science"
          target="_blank" rel="nofollow" title="Computer
          science">computer science</a>.  The <a
          href="https://en.wikipedia.org/wiki/End_user" target="_blank"
          rel="nofollow" title="End user">end user</a> has nothing to
          destroy or corrupt.  Pressing the reset button on the <a
          href="https://en.wikipedia.org/wiki/Personal_computer"
          target="_blank" rel="nofollow" title="Personal
          computer">PC</a> will bring the <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating system">OS</a>
          back to its proper state.</p>

