          <h2 id="Installation">Installing BSD:</h2>

          <p class="lead">Before trying to <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">install</a> <a
          href="https://en.wikipedia.org/wiki/Berkeley_Software_Distribution"
          target="_blank" rel="nofollow" title="Berkeley Software
          Distribution">BSD</a>, get a full inventory of what your <a
          href="https://en.wikipedia.org/wiki/Computer" target="_blank"
          rel="nofollow" title="Computer">computer system</a> has.  You
          might have to help the <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">installer</a> recognize the <a
          href="https://en.wikipedia.org/wiki/Computer_hardware"
          target="_blank" rel="nofollow" title="Hardware">hardware</a>.
          Also make sure that your <a
          href="https://en.wikipedia.org/wiki/Computer_hardware"
          target="_blank" rel="nofollow" title="Hardware">hardware</a>
          manufacturer supports the <a
          href="https://en.wikipedia.org/wiki/Software_distribution"
          target="_self" rel="dofollow" title="Software
          distribution">distribution</a> of your choice &#151; either
          based on <a href="/bsd.php#NetBSD_distros" target="_self"
          rel="dofollow" title="Berkeley Software Distribution &#187
          List of Distributions Based on NetBSD (1993)">NetBSD</a>, <a
          href="/bsd.php#FreeBSD_distros" target="_self" rel="dofollow"
          title="Berkeley Software Distribution &#187 List of
          Distributions Based on FreeBSD (1993)">FreeBSD</a>, <a
          href="/bsd.php#OpenBSD_distros" target="_self" rel="dofollow"
          title="Berkeley Software Distribution &#187 List of
          Distributions Based on OpenBSD (1995)">OpenBSD</a> or <a
          href="/bsd.php#DragonFlyBSD_distros" target="_self"
          rel="dofollow" title="Berkeley Software Distribution &#187
          List of Distributions Based on DragonFly BSD (2003)">DragonFly
          BSD</a>.</p>

          <p class="lead">There are many ways to <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">install</a> <a
          href="https://en.wikipedia.org/wiki/Berkeley_Software_Distribution"
          target="_blank" rel="nofollow" title="Berkeley Software
          Distribution">BSD</a> (from a <a
          href="https://en.wikipedia.org/wiki/CD-ROM" target="_blank"
          rel="nofollow" title="CD-ROM">CD-ROM</a>, <a
          href="https://en.wikipedia.org/wiki/File_Transfer_Protocol"
          target="_blank" rel="nofollow" title="File Transfer
          Protocol">FTP</a>, <a
          href="https://en.wikipedia.org/wiki/Hypertext_Transfer_Protocol"
          target="_blank" rel="nofollow" title="Hypertext Transfer
          Protocol">HTTP</a>, <a
          href="https://en.wikipedia.org/wiki/Network_File_System"
          target="_blank" rel="nofollow" title="Network File
          System">NFS</a>, a <a href="/dos.php" target="_self"
          rel="dofollow" title="Disk Operating System">DOS</a> <a
          href="https://en.wikipedia.org/wiki/Disk_partitioning"
          target="_blank" rel="nofollow" title="Disk
          partitioning">partition</a>, etc.).  If you are a beginner or
          lazy (as <a href="/frn2000.php" target="_self" rel="author"
          title="FRN2000 for VintageOS v133C971">I</a> am), do the <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">installation</a> from a <a
          href="https://en.wikipedia.org/wiki/CD-ROM" target="_blank"
          rel="nofollow" title="CD-ROM">CD-ROM</a>.  Just put the <a
          href="https://en.wikipedia.org/wiki/CD-ROM" target="_blank"
          rel="nofollow" title="CD-ROM">CD-ROM</a> in its <a
          href="https://en.wikipedia.org/wiki/Disk_storage"
          target="_blank" rel="nofollow" title="Disk storage">drive</a>,
          wait and <a href="https://en.wikipedia.org/wiki/Booting"
          target="_blank" rel="nofollow" title="Booting">boot</a>.  Do
          <strong>NOT</strong> forget that the <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">installation</a> will <a
          href="https://en.wikipedia.org/wiki/FORMAT_%28command%29"
          target="_blank" rel="nofollow" title="FORMAT
          (command)">format</a> the <a
          href="https://en.wikipedia.org/wiki/Disk_partitioning"
          target="_blank" rel="nofollow" title="Disk
          partitioning">partition</a> where you <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">install</a> the <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">OS</a>.  If you want to test <a
          href="https://en.wikipedia.org/wiki/Berkeley_Software_Distribution"
          target="_blank" rel="nofollow" title="Berkeley Software
          Distribution">BSD</a> without doing any changes or <a
          href="https://en.wikipedia.org/wiki/FORMAT_%28command%29"
          target="_blank" rel="nofollow" title="FORMAT
          (command)">formating</a> your <a
          href="https://en.wikipedia.org/wiki/Hard_disk_drive"
          target="_blank" rel="nofollow" title="Hard disk
          drive">HDD</a>, get a copy of any live <a
          href="https://en.wikipedia.org/wiki/CD-ROM" target="_blank"
          rel="nofollow" title="CD-ROM">CD-ROM</a> like <a
          href="http://www.freesbie.org/" target="_blank" rel="nofollow"
          title="Free System Burned In Economy">FreeSBIE</a>.</p>

          <p class="lead">A simple way to <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">install</a> <a
          href="https://en.wikipedia.org/wiki/Berkeley_Software_Distribution"
          target="_blank" rel="nofollow" title="Berkeley Software
          Distribution">BSD</a> is to create two main <a
          href="https://en.wikipedia.org/wiki/Disk_partitioning"
          target="_blank" rel="nofollow" title="Disk partitioning">disk
          partitions</a>, a <a
          href="https://en.wikipedia.org/wiki/Paging" target="_blank"
          rel="nofollow" title="Paging">/swap</a> and a system native
          (like <a href="https://en.wikipedia.org/wiki/Ext2"
          target="_blank" rel="nofollow" title="ext2">ext2</a> or <a
          href="https://en.wikipedia.org/wiki/Ext3" target="_blank"
          rel="nofollow" title="ext3">ext3</a>).  As a old rule of
          thumb, make the <a
          href="https://en.wikipedia.org/wiki/Paging" target="_blank"
          rel="nofollow" title="Paging">/swap</a> <a
          href="https://en.wikipedia.org/wiki/Disk_partitioning"
          target="_blank" rel="nofollow" title="Disk
          partitioning">partition</a> (<a
          href="https://en.wikipedia.org/wiki/Virtual_memory"
          target="_blank" rel="nofollow" title="Virtual memory">virtual
          memory</a>, written to <a
          href="https://en.wikipedia.org/wiki/Hard_disk_drive"
          target="_blank" rel="nofollow" title="Hard disk
          drive">HDD</a>) twice the <a
          href="https://en.wikipedia.org/wiki/Random-access_memory"
          target="_blank" rel="nofollow" title="Random-access
          memory">RAM</a> that your <a
          href="https://en.wikipedia.org/wiki/Personal_computer"
          target="_blank" rel="nofollow" title="Personal
          computer">PC</a> has.  For example, if the <a
          href="https://en.wikipedia.org/wiki/Personal_computer"
          target="_blank" rel="nofollow" title="Personal
          computer">PC</a> has 256 <a
          ref="https://en.wikipedia.org/wiki/Megabyte" target="_blank"
          rel="nofollow" title="Megabyte">MB</a> of <a
          href="https://en.wikipedia.org/wiki/Random-access_memory"
          target="_blank" rel="nofollow" title="Random-access
          memory">RAM</a>, assign 512 <a
          href="https://en.wikipedia.org/wiki/Megabyte" target="_blank"
          rel="nofollow" title="Megabyte">MB</a> for <a
          href="https://en.wikipedia.org/wiki/Paging" target="_blank"
          rel="nofollow" title="Paging">/swap</a>.  The rest of the <a
          href="https://en.wikipedia.org/wiki/Hard_disk_drive"
          target="_blank" rel="nofollow" title="Hard disk drive">HDD</a>
          should be <a
          href="https://en.wikipedia.org/wiki/Disk_partitioning"
          target="_blank" rel="nofollow" title="Disk
          partitioning">partitioned</a> as native (<a
          href="https://en.wikipedia.org/wiki/Unix_File_System"
          target="_blank" rel="nofollow" title="Unix File System">Unix
          File System</a> or <a
          href="https://en.wikipedia.org/wiki/Unix_File_System"
          target="_blank" rel="nofollow" title="Unix File
          System">UFS</a>).  To create the <a
          href="https://en.wikipedia.org/wiki/Disk_partitioning"
          target="_blank" rel="nofollow" title="Disk
          partitioning">partitions</a>, refer to the manual included
          with the system that you are <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">installing</a>.</p>

