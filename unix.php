          title="Linux">Linux</a> or <a href="/bsd.php" target="_self"
          rel="dofollow" title="Berkeley Software
          Distribution">BSD</a>), with <a
          href="https://en.wikipedia.org/wiki/Apache_Software_Foundation"
          target="_blank" rel="nofollow" title="Apache Software
          Foundation">Apache Software Foundation</a> (<a
          href="https://en.wikipedia.org/wiki/Apache_Software_Foundation"
          target="_blank" rel="nofollow" title="Apache Software
          Foundation">ASF</a>).  For example, this web site runs on <a
