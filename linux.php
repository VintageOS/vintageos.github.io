          <h2 id="Installation">Installing Linux:</h2>

          <p class="lead">Before trying to <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">install</a> <a
          href="https://en.wikipedia.org/wiki/Linux" target="_blank"
          rel="nofollow" title="Linux">Linux</a>, get a full inventory
          of what your computer has.  You might have to help the <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">installer</a> recognize the <a
          href="https://en.wikipedia.org/wiki/Computer_hardware"
          target="_blank" rel="nofollow" title="Hardware">hardware</a>.
          Also make sure that your <a
          href="https://en.wikipedia.org/wiki/Computer_hardware"
          target="_blank" rel="nofollow" title="Hardware">hardware</a>
          manufacturer supports the <a
          href="https://en.wikipedia.org/wiki/Linux_distribution"
          target="_blank" rel="nofollow" title="Linux
          distribution">Linux distribution</a> of your choice.</p>

          <p class="lead">There are many ways to <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">install</a> <a
          href="https://en.wikipedia.org/wiki/Linux" target="_blank"
          rel="nofollow" title="Linux">Linux</a> (from a <a
          href="https://en.wikipedia.org/wiki/CD-ROM" target="_blank"
          rel="nofollow" title="CD-ROM">CD-ROM</a>, <a
          href="https://en.wikipedia.org/wiki/File_Transfer_Protocol"
          target="_blank" rel="nofollow" title="File Transfer
          Protocol">FTP</a>, <a
          href="https://en.wikipedia.org/wiki/Hypertext_Transfer_Protocol"
          target="_blank" rel="nofollow" title="Hypertext Transfer
          Protocol">HTTP</a>, <a
          href="https://en.wikipedia.org/wiki/Network_File_System"
          target="_blank" rel="nofollow" title="Network File
          System">NFS</a>, a <a href="/dos.php" target="_self"
          rel="nofollow" title="Disk Operating System">DOS</a> <a
          href="https://en.wikipedia.org/wiki/Disk_partitioning"
          target="_blank" rel="nofollow" title="Disk
          partitioning">partition</a>, etc).  If you are a beginner or
          lazy (as <a href="/frn2000.php" target="_self" rel="author"
          title="FRN2000 for VintageOS v133C971">I</a> am), do the <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">installation</a> from a <a
          href="https://en.wikipedia.org/wiki/CD-ROM" target="_blank"
          rel="nofollow" title="CD-ROM">CD-ROM</a>.  Just put the <a
          href="https://en.wikipedia.org/wiki/Disk_storage"
          target="_blank" rel="nofollow" title="Disk storage">disk</a>
          in the <a href="https://en.wikipedia.org/wiki/CD-ROM"
          target="_blank" rel="nofollow" title="CD-ROM">CD-ROM</a> <a
          href="https://en.wikipedia.org/wiki/Disk_storage"
          target="_blank" rel="nofollow" title="Disk storage">drive</a>,
          wait and <a href="https://en.wikipedia.org/wiki/Booting"
          target="_blank" rel="nofollow" title="Booting">boot</a>.  Do
          <strong>NOT</strong> forget that the <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">installation</a> will <a
          href="https://en.wikipedia.org/wiki/FORMAT_%28command%29"
          target="_blank" rel="nofollow" title="FORMAT
          (command)">format</a> the <a
          href="https://en.wikipedia.org/wiki/Disk_partitioning"
          target="_blank" rel="nofollow" title="Disk
          partitioning">partition</a> where you <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">install</a> the <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">OS</a>.  If you want to test <a
          href="https://en.wikipedia.org/wiki/Linux" target="_blank"
          rel="nofollow" title="Linux">Linux</a> without doing any
          changes or <a
          href="https://en.wikipedia.org/wiki/FORMAT_%28command%29"
          target="_blank" rel="nofollow" title="FORMAT
          (command)">formating</a> your <a
          href="https://en.wikipedia.org/wiki/Hard_disk_drive"
          target="_blank" rel="nofollow" title="Hard disk
          drive">HDD</a>, get a copy of any live <a
          href="https://en.wikipedia.org/wiki/Disk_storage"
          target="_blank" rel="nofollow" title="Disk storage">disk</a>,
          which has become common practice nowadays.</p>

          <p class="lead">A simple way to <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">install</a> <a
          href="https://en.wikipedia.org/wiki/Linux" target="_blank"
          rel="nofollow" title="Linux">Linux</a> is to create two main
          <a href="https://en.wikipedia.org/wiki/Disk_storage"
          target="_blank" rel="nofollow" title="Disk storage">disk</a>
          <a href="https://en.wikipedia.org/wiki/Disk_partitioning"
          target="_blank" rel="nofollow" title="Disk
          partitioning">partitions</a>, a <tt><a
          href="https://en.wikipedia.org/wiki/Paging"
          target="_blank" rel="nofollow" title="Paging">/swap</a></tt>
          and a system native.  As a rule of thumb, make the <tt><a
          href="https://en.wikipedia.org/wiki/Paging"
          target="_blank" rel="nofollow" title="Paging">/swap</a></tt>
          <a href="https://en.wikipedia.org/wiki/Disk_partitioning"
          target="_blank" rel="nofollow" title="Disk
          partitioning">partition</a> (<a
          href="https://en.wikipedia.org/wiki/Virtual_memory"
          target="_blank" rel="nofollow" title="Virtual memory">virtual
          memory</a>, written to <a
          href="https://en.wikipedia.org/wiki/Disk_storage"
          target="_blank" rel="nofollow" title="Disk storage">disk</a>)
          twice the <a
          href="https://en.wikipedia.org/wiki/Random-access_memory"
          target="_blank" rel="nofollow" title="Random-access
          memory">RAM</a> that your <a
          href="https://en.wikipedia.org/wiki/Personal_computer"
          target="_blank" rel="nofollow" title="Personal
          computer">PC</a> has.  For example if the <a
          href="https://en.wikipedia.org/wiki/Personal_computer"
          target="_blank" rel="nofollow" title="Personal
          computer">PC</a> has 256 <a
          href="https://en.wikipedia.org/wiki/Megabyte" target="_blank"
          rel="nofollow" title="Megabyte">MB</a> of <a
          href="https://en.wikipedia.org/wiki/Random-access_memory"
          target="_blank" rel="nofollow" title="Random-access
          memory">RAM</a>, assign 512 <a
          href="https://en.wikipedia.org/wiki/Megabyte" target="_blank"
          rel="nofollow" title="Megabyte">MB</a> for <a
          href="https://en.wikipedia.org/wiki/Paging" target="_blank"
          rel="nofollow" title="Paging">/swap</a>.  Allocate the
          remainder of the <a
          href="https://en.wikipedia.org/wiki/Hard_disk_drive"
          target="_blank" rel="nofollow" title="Hard disk drive">HDD</a>
          for the <tt><a href="https://en.wikipedia.org/wiki//root"
          target="_blank" rel="nofollow" title="/root">/root</a></tt> <a
          href="https://en.wikipedia.org/wiki/Disk_partitioning"
          target="_blank" rel="nofollow" title="Disk
          partitioning">partition</a> and make sure it is primary and <a
          href="https://en.wikipedia.org/wiki/Booting" target="_blank"
          rel="nofollow" title="Booting">bootable</a>.  You can also
          create a <a
          href="https://en.wikipedia.org/wiki/Disk_partitioning"
          target="_blank" rel="nofollow" title="Disk
          partitioning">partition</a> for <a
          href="https://en.wikipedia.org/wiki/User_%28computing%29"
          target="_blank" rel="nofollow" title="User
          (computing)">user</a> accounts (<tt><a
          href="https://en.wikipedia.org/wiki/Home_directory"
          target="_blank" rel="nofollow" title="Home
          directory">/home</a></tt>) and another for <a
          href="https://en.wikipedia.org/wiki/User_%28computing%29"
          target="_blank" rel="nofollow" title="User
          (computing)">user</a> <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">installations</a> (<tt>/usr</tt>).  Neither of the
          latter two <a
          href="https://en.wikipedia.org/wiki/Disk_partitioning"
          target="_blank" rel="nofollow" title="Disk
          partitioning">partitions</a> are destroyed when <a
          href="https://en.wikipedia.org/wiki/Upgrade" target="_blank"
          rel="nofollow" title="Upgrade">upgrading</a> the <a
          href="https://en.wikipedia.org/wiki/Operating_system"
          target="_blank" rel="nofollow" title="Operating
          system">OS</a>.  To create the <a
          href="https://en.wikipedia.org/wiki/Disk_partitioning"
          target="_blank" rel="nofollow" title="Disk
          partitioning">partitions</a>, refer to the manual included
          with the <a href="https://en.wikipedia.org/wiki/Computer"
          target="_blank" rel="nofollow" title="Computer">system</a>
          that you are <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">installing</a>.  A good technical reference is the
          downloadable <a href="http://www.slackbook.org/#book"
          target="_blank" rel="nofollow" title="Slackware
          Book">Slackware Book</a> even if <a
          href="https://en.wikipedia.org/wiki/Slackware" target="_blank"
          rel="nofollow" title="Slackware">Slackware</a> is not your <a
          href="https://en.wikipedia.org/wiki/Linux_distribution"
          target="_blank" rel="nofollow" title="Linux
          distribution">Linux distribution</a> of choice.</p>

          <p class="lead">Then again, if you want to <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">install</a> <a
          href="https://en.wikipedia.org/wiki/Linux" target="_blank"
          rel="nofollow" title="Linux">Linux</a> easily and painlessly
          with mere questions to <a
          href="https://en.wikipedia.org/wiki/Computer_configuration"
          target="_blank" rel="nofollow" title="Computer
          configuration">configure</a> the <a
          href="https://en.wikipedia.org/wiki/Computer" target="_blank"
          rel="nofollow" title="Computer">system</a>, you can try <a
          href="https://en.wikipedia.org/wiki/Ubuntu_%28operating_system%29"
          target="_blank" rel="nofollow" title="Ubuntu (operating
          system)">Ubuntu</a> based on <a
          href="https://en.wikipedia.org/wiki/Debian" target="_blank"
          rel="nofollow" title="Debian">Debian</a>.  <a
          href="/frn2000.php" target="_self" rel="author" title="FRN2000
          for VintageOS v133C971">I</a> have been running a flavor of <a
          href="https://en.wikipedia.org/wiki/Ubuntu_%28operating_system%29"
          target="_blank" rel="nofollow" title="Ubuntu (operating
          system)">Ubuntu</a> for a couple of years already without any
          complaints, other than <a
          href="https://en.wikipedia.org/wiki/Installation_%28computer_programs%29"
          target="_blank" rel="nofollow" title="Installation (computer
          programs)">installing</a> the <a
          href="https://en.wikipedia.org/wiki/Codec" target="_blank"
          rel="nofollow" title="Codec">codecs</a> to play <a
          href="https://en.wikipedia.org/wiki/DVD" target="_blank"
          rel="nofollow" title="DVD">DVD</a>s manually.</p>

