          <h1 id="main">What is OS/2?</h1>

          <blockquote>
            <em>&#34;We believe that <a
            href="https://en.wikipedia.org/wiki/OS%2F2" target="_blank"
            rel="nofollow" title="OS/2">OS/2</a> is the platform for the
            90s.&#34;</em>
            <br/>&#151; Bill Gates
          </blockquote>

          <p class="lead" id="1987"><a
          href="https://en.wikipedia.org/wiki/OS%2F2" target="_blank"
          rel="nofollow" title="OS/2">OS/2</a> stands for <a
          href="https://en.wikipedia.org/wiki/OS%2F2" target="_blank"
          rel="nofollow" title="OS/2">Operating System/2</a>.  <a
          href="https://en.wikipedia.org/wiki/IBM" target="_blank"
          rel="nofollow" title="IBM">IBM</a> and <a
          href="https://en.wikipedia.org/wiki/Microsoft" target="_blank"
          rel="nofollow" title="Microsoft">Microsoft</a> <a
          href="https://en.wikipedia.org/wiki/Software_development_process"
          target="_blank" rel="nofollow" title="Software development
          process">developed</a> <a
          href="https://en.wikipedia.org/wiki/OS%2F2" target="_blank"
          rel="nofollow" title="OS/2">MS OS/2</a>, the first version of
          this multi-tasking <a
          href="https://en.wikipedia.org/wiki/16-bit" target="_blank"
          rel="nofollow" title="16-bit">16-bit</a> system that was
          intended to replace <a href="/dos.php" target="_self"
          rel="dofollow" title="Disk Operating System"</a> and <a
          href="/windows.php" target="_self" rel="dofollow"
          title="Microsoft Windows">Windows 3.x</a>.  <a
          href="https://en.wikipedia.org/wiki/Microsoft" target="_blank"
          rel="nofollow" title="Microsoft">Microsoft</a> took part of
          the code to <a
          href="https://en.wikipedia.org/wiki/Software_development_process"
          target="_blank" rel="nofollow" title="Software development
          process">develop</a> <a href="/windows.php" target="_self"
          rel="dofollow" title="Microsoft Windows">Windows NT 3.x</a>.
          <a href="https://en.wikipedia.org/wiki/IBM" target="_blank"
          rel="nofollow" title="IBM">IBM</a> continued with the <a
          href="https://en.wikipedia.org/wiki/Software_development_process"
          target="_blank" rel="nofollow" title="Software development
          process">development</a> of <a
          href="https://en.wikipedia.org/wiki/OS%2F2" target="_blank"
          rel="nofollow" title="OS/2">OS/2</a> till 1997/2000 releasing
          versions 2 to 4, which are <a
          href="https://en.wikipedia.org/wiki/32-bit" target="_blank"
          rel="nofollow" title="32-bit">32-bit</a> <a
          href="https://en.wikipedia.org/wiki/Computer" target="_blank"
          rel="nofollow" title="Computer">systems</a>.  <a
          href="https://en.wikipedia.org/wiki/OS%2F2" target="_blank"
          rel="nofollow" title="OS/2">Versions 3</a> and <a
          href="https://en.wikipedia.org/wiki/OS%2F2" target="_blank"
          rel="nofollow" title="OS/2">4</a> were released under the name
          <a href="https://en.wikipedia.org/wiki/OS%2F2" target="_blank"
          rel="nofollow" title="OS/2">OS/2 Warp</a>.</p>

